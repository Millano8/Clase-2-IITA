class Calculadora:

    def sumar(a, b) -> int:
        """Retorna la suma de dos numeros enteros"""
        return a+b

    def restar(a, b) -> int:
        """Retorna la resta de dos numeros enteros"""
        return a-b

    def multiplicar(a, b) -> int:
        """Retorna la multiplicacion de dos numeros enteros"""
        return a*b

    def dividir(a, b) -> float:
        """Retorna la division de dos numeros de punto flotante"""
        return a/b

    def cuadrado(a, b) -> int:
        """Retorna la potencia de un numero 'a' elevado por otro 'b'."""
        return a**b
